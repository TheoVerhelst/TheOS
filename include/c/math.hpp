#ifndef MATH_HPP
#define MATH_HPP

/// Get the absolute value of the given number.
/// \param n A number.
/// \return n > 0 ? n : -n;
int abs(int n);

/// Get the absolute value of the given number.
/// \param n A number.
/// \return n > 0L ? n : -n;
long int abs(long int n);

/// Get the absolute value of the given number.
/// \param n A number.
/// \return n > 0LL ? n : -n;
long long int abs(long long int n);

#endif// MATH_HPP
